    private void tfDNIActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tfDNIActionPerformed

        nuevoDueño = UsuariosControlador.getDueñoDNI(tfDNI.getText());
        if (nuevoDueño != null) {
            Object[] fila = {nuevoDueño.getDni(), nuevoDueño.getNombre(), nuevoDueño.getTelefono()};
            tm.addRow(fila);
        } else {
            jblResultado.setText("¡Dueño no encontrado!");
        }
    }//GEN-LAST:event_tfDNIActionPerformed

