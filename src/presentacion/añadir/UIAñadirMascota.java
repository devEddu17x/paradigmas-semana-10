    private void btnAñadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAñadirActionPerformed
        boolean exito = MascotasControlador.agregarMascota(tfNombre.getText(), tfEspecie.getText(), Integer.parseInt(tfDia.getText()), Integer.parseInt(tfMes.getText()), Integer.parseInt(tfAño.getText()), nuevoDueño);
        if (exito) {
            System.out.println("Se añadio a la mascota");
            this.dispose();
        } else {
            jblResultado.setText("No se pudo añadir la mascota");
        }
    }//GEN-LAST:event_btnAñadirActionPerformed

